//
//Входные данные
//В этой задаче нет входных данных. Вам необходимо лишь реализовать функцию с именем sum, которая принимает два целых числа типа int.
//
//Выходные данные
//Ваша функция должна вернуть одно числа типа int - сумму данных чисел.

